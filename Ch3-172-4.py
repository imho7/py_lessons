# Страница 172 Упражение 4 Римские цифры

num = int(input('Введите арабску цифру в диапазоне от 1 до 10: '))

if num == 1:
    print('Римская цифра - I')
elif num == 2:
    print('Римская цифра - II')
elif num == 3:
    print('Римская цифра - III')
elif num == 4:
    print('Римская цифра - IV')
elif num == 5:
    print('Римская цифра - V')
elif num == 6:
    print('Римская цифра - VI')
elif num == 7:
    print('Римская цифра - VII')
elif num == 8:
    print('Римская цифра - VIII')
elif num == 9:
    print('Римская цифра - IX')
elif num == 10:
    print('Римская цифра - X')
else:
    print('Введите число из предлагаемого диапазона!')
