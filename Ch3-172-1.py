dayWeek = int(input('Введите день недели от 1 до 7: '))

if dayWeek == 1:
    print('Сегодня Понедельник')
elif dayWeek == 2:
    print('Сегодня Вторник')
elif dayWeek == 3:
    print('Сегодня Среда')
elif dayWeek == 4:
    print('Сегодня Четверг')
elif dayWeek == 5:
    print('Сегодня Пятница')
elif dayWeek == 6:
    print('Сегодня Суббота')
elif dayWeek == 7:
    print('Сегодня Воскресенье')
else:
    print('Какой-то непонятный день недели!')
